package com.lianjia.springbootsql.bean;

public class Student {
    private int id;
    private String name;
    private int classId;
    private int aaa;
    private Class class11;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getClassId() {
        return classId;
    }

    public void setClassId(int classId) {
        this.classId = classId;
    }

    public int getAaa() {
        return aaa;
    }

    public void setAaa(int aaa) {
        this.aaa = aaa;
    }

    public Class getClass11() {
        return class11;
    }

    public void setClass11(Class class11) {
        this.class11 = class11;
    }
}
